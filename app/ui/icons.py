def get_icon(name, color=None, size=None):
    """
    Get a QIcon object for the given name
    
    Args:
        name: Icon name from the dictionary
        color: Optional color to apply (not used yet)
        size: Optional size for the icon
        
    Returns:
        QIcon: The icon or a default icon if not found
    """
    if name not in _ICONS:
        # Return a default icon if the name is not found
        return QIcon()
    
    # Get SVG data
    svg_data = _ICONS[name]
    
    # Create QIcon from SVG data
    byte_array = QByteArray(svg_data.encode('utf-8'))
    renderer = QSvgRenderer(byte_array)
    
    # Create icon with renderer
    icon = QIcon()
    
    # Add specific sizes if requested
    if size:
        qsize = QSize(size, size)
        icon.addPixmap(QPixmap(qsize), QIcon.Normal, QIcon.Off)
    else:
        # Add standard sizes
        for size in [16, 24, 32, 48]:
            qsize = QSize(size, size)
            icon.addPixmap(QPixmap(qsize), QIcon.Normal, QIcon.Off)
    
    return icon
